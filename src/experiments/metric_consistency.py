"""Metric consistency across encoder pairs (Spearman rank).

Paper binding (LaTeX labels):
    Tab.~``tab:metric-consistency``;
    body §``sec:metrics`` claim that the four metrics (mutual-kNN,
    debiased CKA, null-calibrated z, unbiased CKA) produce consistent
    rankings of encoder pairs.

Input:
    experiments/alignment_matrix_*/results.csv  (from alignment_matrix.py)

Process:
    For each metric m, compute a ranking over encoder pairs (ordered by
    alignment value). Then compute Spearman rank correlation between
    every pair of metrics. Output the resulting metric-vs-metric
    correlation matrix as CSV + LaTeX.

Usage:
    python -m src.experiments.metric_consistency \
        --input experiments/alignment_matrix_full/results.csv \
        --output-dir experiments/metric_consistency_full \
        --latex-path paper/tables/metric_consistency_table.tex

Output:
    - experiments/metric_consistency_full/correlation_matrix.csv
    - paper/tables/metric_consistency_table.tex
"""
from __future__ import annotations
import argparse
import csv
from pathlib import Path

import numpy as np
from scipy.stats import spearmanr


def load_records(csv_path: Path) -> list[dict]:
    with csv_path.open() as f:
        return [dict(row) for row in csv.DictReader(f)]


def pair_to_key(row: dict) -> str:
    """Order-invariant encoder-pair key."""
    a, b = sorted([row["encoder_a"], row["encoder_b"]])
    return f"{a}||{b}"


def metric_rankings(records: list[dict]) -> tuple[list[str], dict[str, np.ndarray]]:
    """Return (ordered_pair_list, {metric: values in same order})."""
    pairs = sorted({pair_to_key(r) for r in records})
    metrics = sorted({r["metric"] for r in records})
    by_metric: dict[str, np.ndarray] = {}
    for m in metrics:
        vals = []
        for p in pairs:
            a, b = p.split("||")
            val = next(
                (float(r["value"]) for r in records
                 if r["metric"] == m and pair_to_key(r) == p),
                np.nan,
            )
            vals.append(val)
        by_metric[m] = np.array(vals)
    return pairs, by_metric


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--input", type=str,
                        default="experiments/alignment_matrix_full/results.csv")
    parser.add_argument("--output-dir", type=str, default="experiments/metric_consistency_full")
    parser.add_argument("--latex-path", type=str,
                        default="paper/tables/metric_consistency_table.tex")
    args = parser.parse_args()

    in_path = Path(args.input)
    if not in_path.exists():
        raise FileNotFoundError(
            f"Input CSV not found: {in_path}. Run alignment_matrix.py first."
        )

    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    records = load_records(in_path)
    pairs, by_metric = metric_rankings(records)
    print(f"[tbl2] loaded {len(records)} records, {len(pairs)} unique encoder pairs")
    metrics = sorted(by_metric.keys())
    print(f"[tbl2] metrics: {metrics}")

    # Compute Spearman correlation matrix
    n = len(metrics)
    corr = np.zeros((n, n))
    pval = np.zeros((n, n))
    for i, mi in enumerate(metrics):
        for j, mj in enumerate(metrics):
            rho, p = spearmanr(by_metric[mi], by_metric[mj])
            corr[i, j] = rho
            pval[i, j] = p

    # Write CSV
    csv_path = out_dir / "correlation_matrix.csv"
    with csv_path.open("w") as f:
        w = csv.writer(f)
        w.writerow(["metric"] + metrics)
        for i, mi in enumerate(metrics):
            w.writerow([mi] + [f"{corr[i, j]:.4f}" for j in range(n)])
    print(f"[tbl2] wrote {csv_path}")

    # Print summary
    print("\nSpearman rank correlation between metrics:")
    header = f"{'':18s}" + " ".join(f"{m:>14s}" for m in metrics)
    print(header)
    for i, mi in enumerate(metrics):
        row = f"{mi:18s}" + " ".join(f"{corr[i, j]:+14.4f}" for j in range(n))
        print(row)

    # LaTeX table
    def fmt_label(m: str) -> str:
        return {
            "mutual_knn": r"$A_{\mathrm{kNN}}$",
            "debiased_cka": r"$A_{\mathrm{dCKA}}$",
            "unbiased_cka": r"$A_{\mathrm{uCKA}}$",
            "null_knn_z": r"$A_{\mathrm{null}}$",
        }.get(m, m.replace("_", r"\_"))

    latex_lines = [
        "% Table 2: Metric consistency (Spearman rank correlation)",
        "% Generated by src/experiments/metric_consistency.py",
        r"\begin{table}[t]",
        r"\centering",
        r"\caption{Spearman rank correlation between the four alignment metrics computed over "
        + f"all encoder-pair values in Figure 1 ({len(pairs)} pairs). "
        + "Near-1.0 correlations on all off-diagonals indicate that the four metrics produce "
        + "a consistent ranking of encoder pairs, supporting our claim that the headline "
        + "findings do not depend on the choice of alignment metric.}",
        r"\label{tab:metric-consistency}",
        r"\begin{tabular}{l" + "c" * n + "}",
        r"\toprule",
        " & " + " & ".join(fmt_label(m) for m in metrics) + r" \\",
        r"\midrule",
    ]
    for i, mi in enumerate(metrics):
        row = fmt_label(mi) + " & " + " & ".join(
            f"{corr[i, j]:+.3f}" for j in range(n)
        ) + r" \\"
        latex_lines.append(row)
    latex_lines.append(r"\bottomrule")
    latex_lines.append(r"\end{tabular}")
    latex_lines.append(r"\end{table}")

    latex_path = Path(args.latex_path)
    latex_path.parent.mkdir(parents=True, exist_ok=True)
    with latex_path.open("w") as f:
        f.write("\n".join(latex_lines) + "\n")
    print(f"[tbl2] wrote {latex_path}")


if __name__ == "__main__":
    main()
